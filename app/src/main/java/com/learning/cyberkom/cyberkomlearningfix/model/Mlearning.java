package com.learning.cyberkom.cyberkomlearningfix.model;

public class Mlearning {

    public String getid;
    public String nameurl;
    public String video;
    public String nameurl2;
    public String video2;
    public String linkkuis;
    public String linknilai;
    public String date;

    public Mlearning(String getid, String nameurl, String video, String nameurl2, String video2, String linkkuis, String linknilai, String date) {
        this.getid = getid;
        this.nameurl = nameurl;
        this.video = video;
        this.nameurl2 = nameurl2;
        this.video2 = video2;
        this.linkkuis = linkkuis;
        this.linknilai = linknilai;
        this.date = date;
    }

    public String getGetid() {
        return getid;
    }

    public void setGetid(String getid) {
        this.getid = getid;
    }

    public String getNameurl() {
        return nameurl;
    }

    public void setNameurl(String nameurl) {
        this.nameurl = nameurl;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }

    public String getNameurl2() {
        return nameurl2;
    }

    public void setNameurl2(String nameurl2) {
        this.nameurl2 = nameurl2;
    }

    public String getVideo2() {
        return video2;
    }

    public void setVideo2(String video2) {
        this.video2 = video2;
    }

    public String getLinkkuis() {
        return linkkuis;
    }

    public void setLinkkuis(String linkkuis) {
        this.linkkuis = linkkuis;
    }

    public String getLinknilai() {
        return linknilai;
    }

    public void setLinknilai(String linknilai) {
        this.linknilai = linknilai;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
